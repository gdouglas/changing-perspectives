            <h1>About Us</h1>
            <p>
                The Vancouver Maritime Museum is located on the unceded
                territories of the xʷməθkwəy̓əm (Musqueam), Skwxwú7mesh
                (Squamish), and səlilwətaɬ (Tsleil-Waututh) Nations in Vancouver
                British Columbia, Canada.
            </p>
            <p>
                For over 60 years, the Vancouver Maritime Museum has been
                sharing stories about the Pacific Northwest and the Arctic and
                creating experiences that build bridges between the past and
                future.
            </p>
            <p>
                We invite a diverse range of voices to share their perspectives,
                providing a place of connection and community and building a
                shared sense of belonging.
            </p>
