    <h3 class="gold">Sea Otter Pelts</h3>
    <figure>
        <img src="/images/encounters/sea-otter-pelts.jpg" alt="Two black and white photos. In one, a man wearing a bowler hat poses against a shiplap wall with three oval pelts. In the other a man stands beside one long pelt and a small baby pelt. In pen the first image has 'Sea Otter Skin. 1892 Ounakaska' written. The other has 'Sea Otter Skin Ounalaska.'" loading="lazy">
        <figcaption>
            <p>Sea otter skins, Unalaska, Alaska, 1892</p>
            <p>Archival Photographer Stefan Claesson</p>
            <p>Gulf of Maine Cod Project, NOAA National Marine Sanctuaries; Courtesy of National Archives</p>
            <p>These 1892 photos from Alaska show how large sea otter pelts are. They have a soft, thick coat of fur to keep them warm in the Pacific Ocean.</p>
            <p>Indigenous peoples on the coast have hunted sea otters for food and fur for thousands of years. They traded the pelts with overseas fur traders starting in the 1700s. Sailors found the pelts beautiful and rare, and could sell them in China for a small fortune. Within 200 years, sea otters were almost hunted to extinction on the coast.</p>
        </figcaption>
    </figure>
