<p>Téléscope en deux morceaux, recouvert de cuir, v. 1790.</p>

<p>Fabriqué par G. ou P. Dolland, avec inscription «&nbsp;Capt. John. Baker Royal Navy&nbsp;», prêté au Vancouver Maritime Museum, courtoisie de Richard Baker. Joseph Baker était le lieutenant de Vancouver. Un télescope est un instrument optique qui magnifie les objets éloignés en disposant des lentilles ou des miroirs incurvés de façon à recueillir et focaliser la lumière.</p>
                    </figcaption>
