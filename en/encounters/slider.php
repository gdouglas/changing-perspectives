            <h2>Panel 3</h2>
            <div id="panel-three-transcript">
                <p>&ldquo;For several decades the fur trade was the main
                concern of European + US ships entering the region&mldr;</p>
                <p>This trade decimated the sea otter population. Ship&rsquo;s crews used force at times to ensure compliance, even taking hostages.&rdquo;</p>
                <p>(Hand-drawn, black and white drawing of two sea otters swimming.)</p>
            </div>
        </div>
        <div id="card-3" tabindex="0">
            <h2>Panel 4</h2>
            <div id="panel-four-transcript">
                <p>&ldquo;The Russians began trading with the Aleut in 1741. They took families hostage &amp; forced hunters to gather sea otters.</p>
                <p>In 1763, Aleuts revolted + destroyed 4 of 5 Russian ships. In retaliation, many Aleut villagers were massacred.&rdquo;</p>
                <p>(A Russian in a fur hat holds a musket at an Aleut villager who&rsquo;s hands are up in surrender. In the background is a sail ship.)</p>
            </div>
        </div>
        <div id="card-4" tabindex="0">
            <h2>Panel 5</h2>
            <div id="panel-five-transcript">
                <p>&ldquo;As the Russians ravaged the Aleut &amp; sea otter populations, they began moving south into Tlingit territory. But the Tlingit were much better armed from trading with British &amp; American ships.</p>
                <p>The most sought-after trade items were guns &amp; ammo. Throughout the Coast, women conducted most of the trade negotiations.&rdquo;</p>
                <p>(Three Tlingit armed with muskets face-off a European on a sail ship. The European is in a jacket and also armed with a musket.)</p>
            </div>
        </div>
        <div id="card-5" tabindex="0">
            <h2>Panel 6</h2>
            <div id="panel-six-transcript">
                <p>&ldquo;As more ships descended on the region throughout the 1780s &amp; &lsquo;90s, more ships were attacked. Over the next 30 years dozens of ships would be captured.&rdquo;</p>
                <p>(Warriors in traditional armour with spears and clubs board the side of a ship armed with Europeans.)</p>
            </div>
        </div>
        <div id="card-6" tabindex="0">
            <h2>Panel 7</h2>
            <div id="panel-seven-transcript">
                <p>In 1803, the US ship Boston was attacked by Mowachaht &amp;
                Muchalaht warriors, who killed 25 or 27 crew members.</p>
                <p>The 2 survivors were forced to identify the severed heads of the crew. They were enslaved for 2 years&mldr;&rdquo;</p>
                <p>(On the side of a ship is a row of severed heads. Warriors face them holding spears and clubs while one American points at the heads.)</p>
            </div>
        </div>
