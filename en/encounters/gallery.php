    <h3 class="gold">Nuu-chah-nulth Fish Hook</h3>
    <figure>
        <img src="/images/encounters/nuu-chah-nulth-fish-hook.jpg" alt="A thin peice of wood curves into a 'U' shape with a curve on one end and a sharp tusk pointed inwards on the other. The tusk is attached with leather and twine. The other end has leather wound around it for holding a line." loading="lazy">
        <figcaption>
            <p>Halibut fish hook, ca. 1790s.</p>
            <p>Courtesy of The British Museum</p>
            <p>Catalogue Number: Am, VAN.131.b</p>
            <p>Nuu-chah-nulth designed this hook to catch halibut, a large flatfish that swims along the ocean floor. The hook is bent from wood. It is 12.5cm long and 12.5cm wide.  A piece of bait would be tied onto the bone barb. Then the hook would be lowered down into the water on a rope.</p>
            <p>Captain Vancouver's crew were often hungry because they didn't know how to hunt and fish on the Pacific Northwest Coast. They traded for Indigenous fishing gear, but also relied on Indigenous communities for food and fresh water. This fish hook was collected by the surgeon's mate on the HMS Discovery, George Goodman Hewett, from the Mowachaht/Muchalaht First Nation at Nootka Sound. </p>
        </figcaption>
    </figure>
</div>
