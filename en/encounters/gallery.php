<p>Halibut fish hook, ca. 1790s. Courtesy of The British Museum Catalogue Number: Am, VAN.131.b.</p>

<p>Nuu- chah-nulth designed this hook to catch halibut, a large flatfish that swims along the ocean floor. The hook is bent wood. It is 12.5cm long and 12.5cm wide. A piece of bait would be tied onto the bone barb. Then the hook would be lowered down into the water on a rope. Captain Vancouver&#39;s crew were often hungry because they didn&#39;t know how to hunt and fish on the Pacific Northwest Coast. They traded for Indigenous fishing gear, but also relied on Indigenous communities for food and fresh water. This fish hook was collected by the surgeon&#39;s mate on the HMS Discovery, George Goodman Hewett, from the Mowachaht/Muchalaht First Nation at Yuquot.</p></figcaption>
