            <h2>Panneau 1</h2>
            <p>Cette bande dessinée est tirée de « L’album de 500 ans de résistance autochtone » (The 500 Years of Indigenous Resistance Comic Book) créé par l’artiste et historien Kwakwaka’wakw Gord Hill.</p>
            <p><strong>AVERTISSEMENT: </strong>Certaines images sont de nature très crue et peuvent être troublantes.</p>
        </div>
        <div id="card-1">
            <h2>Panneau 2</h2>
            <p>« Comment les Européens ont-ils colonisé la côte nord-ouest du Pacifique? » (Le titre est en blanc sur fond noir avec une police de caractères rappelant l’écriture à la craie. Toutes les images suivantes sont dessinées à la main et sont en noir et blanc.)</p>
        </div>
        <div id="card-2">
            <h2>Panneau 3</h2>
            <p id="panel-two-transcript">« Dans les années 1820, la Compagnie de la Baie d’Hudson constituait le principal pouvoir colonial de la région, même si le commerce des fourrures était en déclin. La compagnie détenait de nombreux forts et de nombreux bateaux. En 1828, un village Klallam, dans l’État de Washington, fut détruit par une canonnière de la Compagnie. » (Un dessin d’hommes sur une canonnière chargeant des canons et tirant sur un village côtier.)</p>
        </div>
        <div id="card-3">
            <h2>Panneau 4</h2>
            <p id="panel-three-transcript">« En 1849, l’île de Vancouver fut déclarée colonie officielle avec la CBH agissant à titre de gouvernement. » (Un dessin d’un fort avec un drapeau britannique.)</p>
        </div>
        <div id="card-4">
            <h2>Panneau 5</h2>
            <p id="panel-four-transcript">« Au même moment, l’établissement de colons et la vente de terres étaient devenus des sujets cruciaux pour le gouvernement… Pendant 30 ans, les canonnières de la Marine royale ont été utilisées pour servir le colonialisme britannique et contrôler les tribus côtières. » (Un dessin de trois hommes pointant une carte intitulée « Colonie de l’île de Vancouver ». Un des hommes dit : « Les Kwakiutl sont toujours des chasseurs de têtes? », ce à quoi répond un autre homme : « Oui monsieur… ») </p>
        </div>
        <div id="card-5">
            <h2>Panneau 6</h2>
            <p id="panel-five-transcript">« Les canonnières servaient de forces militaire et policière… Elles étaient lourdement armées avec 50 canons et roquettes ainsi que des soldats de la Marine Royale. » (Un dessin de canons enlignés d’un côté d’une canonnière et d’un soldat de la Marine en uniforme qui regarde vers la rive, un mousquet entre les mains.)</p>
        </div>
        <div id="card-6">
            <h2>Panneau 7</h2>
            <p id="panel-six-transcript">« En 1862, une épidémie de variole se déclara à Victoria. Les autorités coloniales ont forcé une centaine d’Autochtones à s’en aller. Infectés, ils sont retournés dans leurs villages et ont répandu la maladie. On estime qu’une personne sur trois est morte en 2 ans. L’épidémie s’est déclarée avec l’arrivée d’un plus grand nombre de colons européens. » (Un dessin d’une femme couverte de variole morte sur la plage. Un corbeau picosse son visage. Derrière il y a une maison longue et deux autres morts sur la plage.)</p>
        </div>
        <div id="card-7">
            <h2>Panneau 8</h2>
            <p id="panel-seven-transcript">« En 1871, la C.-B. est entrée dans le Canada. En 1874, la Loi relative aux terres de la C.-B. fut adoptée pour ouvrir le territoire à la colonisation. Le Canada émit le Désaveu de 1875, supprimant ainsi la Loi relative aux terres de la C.-B., citant l’échec de la province à conclure des traités stipulant légalement la cession des terres par les Autochtones. » (Une main tient un papier intitulé « Devoir de désaveu ».)</p>
        </div>
        <div id="card-8">
            <h2>Panneau 9</h2>
            <p id="panel-eight-transcript">« En réponse à cela, la C.-B. menaça de se retirer du Canada… En 1876, le Canada adopta la Loi sur les Indiens, qui avait pour effet d’étendre le contrôle gouvernemental sur tous les Autochtones, incluant ceux de la C.-B., " légalisant " ainsi le vol des territoires autochtones ». (Une main tient un papier intitulé « Loi sur les Indiens de 1876 ». À côté, il y a d’autres papiers intitulés « Conseils de bande », « Réserves » et « Statut ».)</p>
        </div>
