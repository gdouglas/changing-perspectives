            <h1>Qui sommes-nous?</h1>
            <p>Le Vancouver Maritime Museum est situé sur les territoires non-cédés des nations xʷməθkwəy̓əm (Musqueam), Skwxwú7mesh (Squamish), et səlilwətaɬ (Tsleil-Waututh) à Vancouver, en Colombie-Britannique, au Canada.</p>
            <p>Depuis plus de 60 ans, le Vancouver Maritime Museum partage des histoires sur la côte nord-ouest du Pacifique et sur l’Arctique et crée des expériences qui bâtissent des ponts entre le passé et le futur.</p>
            <p>Nous invitons un large éventail de voix différentes à partager leurs perspectives, offrant un lieu de connexion et de communauté où se crée un sentiment partagé d’appartenance.</p>
            <h2>Crédits et remerciements</h2>
            <p>Ce projet en ligne a été développé avec l’appui du programme d’investissement de Musées numériques Canada. Musées numériques Canada est géré par le Musée canadien de l’histoire avec l’aide financière du gouvernement du Canada.</p>
            <p>Le Vancouver Maritime Museum remercie grandement le Musée canadien de l’histoire pour l’investissement financier octroyé pour cette présentation en ligne pour Musées numériques Canada.</p>
            <p>L’exposition Nouvelles perspectives a été créée entre 2018 et 2022 par l’équipe d’exposition et une foule d’autres personnes, des donateurs et de partenaires de projet qui ont permis de rassembler toutes ces histoires.</p>
