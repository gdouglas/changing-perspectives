    <h3 class="gold">Cat-O'-Nine Tails </h3>
    <figure>
        <img src="/images/encounters/cat-o-nine-tails.jpg" alt="A wooden rod with a short leather strap bound to it's end. The leather strap has nine cords tied to it's end. The cords have multiple knots tied to them. The bottom of the rod shows heavy signs of wear from being handled." loading="lazy">
        <figcaption>
            <p>Cat-o'-nine tails whip, ca. 1700-1850</p>
            <p>Courtesy of Wellcome Collection </p>
            <p>A cat-o'-nine tails was a whip used by the British Royal Navy to punish sailors. It consists of nine pieces of cord each tied with a series of knots. Sailors could receive up to 24 whip lashes across their backs at a time. Like other Royal Navy commanders, Captain Vancouver regularly punished his crew this way.</p>
        </figcaption>
