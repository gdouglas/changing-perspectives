<p>Haíɫzaqv (Heiltsuk) artists painted and carved rock faces at villages and other sites within their respective territories. Artists used red dyes from local plants to illustrate family crests, coppers, and possibly historical events. Heiltsuk archaeologist Elroy White (Q̓íx̌itasu) thinks this rock art could represent Vancouver&#39;s expedition arriving in a small boat at the village site. Others believe it could be depicting either a serpent’s mouth or an unidentified flying object (UFO). Collectively, this body of rock art marks the Heiltsuk&#39;s historical and unbroken occupation of their territory.</p>
