                                <p>How did Captain Vancouver navigate the oceans?</p>
                                <p>Well, he relied on a magnetic compass to know which direction the ship was sailing.</p>
                                <p>While the navigator knew the ship&rsquo;s general direction, the problem was figuring out how far the ship had travelled once he had lost sight of the land.</p>
                                <p>North or south was easy to determine using latitude; a method developed by Arabian Navigators in the 9th century.&nbsp;</p>
                                <p>They applied imaginary lines around the earth east to west, and numbered them from zero degrees at the equator to 90 degrees at each of the poles.</p>
                                <p>Explorers would then use math rules relating to triangles called trigonometry to measure the angle between the horizon and the North Star.</p>
                                <p>&nbsp;The North Star- also called Polaris- is the perfect point to measure from because it barely changes its position &ndash; and is conveniently located right above the North Pole.</p>
                                <p>With an instrument called a sextant, the sailing crew could measure the angle, and if it said, say, 50 degrees, that would mean their ship is located at 50 degrees south of the North Pole. 90 degrees minus 50 degrees equals 40 degrees. So the ship is located at latitude 40 degrees north of the equator.</p>
                                <p>Pretty cool, right?</p>
                                <p>Using this method, the crew could know exactly how far they&rsquo;ve travelled north or south, and keep perfectly positioned until they spotted land again.</p>
                                <p>However, figuring out how far east or west the ship had travelled remained a big problem.</p>
                                <p>To solve this, Captain Vancouver was able to use an innovative new method that had just been perfected by his predecessor, Captain Cook.</p>
                                <p>The solution relied on the regular rotation of the earth in relation to the sun, and a very accurate clock.</p>
                                <p>Here&rsquo;s how it works.</p>
                                <p>We know that the earth rotates from east to west 360 degrees every 24 hours.</p>
                                <p>Just do the math, and 360 divides by 24 to make, 15. So, in one hour the earth moves 15 degrees relative to the sun.</p>
                                <p>Still with me?</p>
                                <p>Just like latitude, degrees of longitude were established to mark new imaginary lines around the earth, but these lines run north to south.</p>
                                <p>And because the British thought this idea up, they decided that the zero degrees longitude line - or the Prime Meridian - should run right through the Greenwich observatory, located in London England.</p>
                                <p>If you have an accurate clock on board your ship and set it to the time at Greenwich &ndash; Greenwich Mean Time - you can then compare Greenwich time against the ship&rsquo;s local time to determine how far you are east or west of Greenwich.</p>
                                <p>Here&rsquo;s an example, First the navigator has to determine the local noon time where the ship is located. He does this with a sextant to measure when the sun is at its highest point in the sky. If at 12 noon local time our clock shows Greenwich Mean Time is 4 in the afternoon, then we can calculate that we are 4 hours behind Greenwich time.</p>
                                <p>Knowing that one hour is 15 degrees - we must be 4 hours times 15 degrees behind Greenwich, So, 60 degrees west of England.&nbsp; Simple, Right?</p>
                                <p>While this method of determining longitude was fairly easy to understand, it still required a super reliable clock in order to do it. And a clock accurate enough for the job did not exist.</p>
                                <p>In 1714 the British government offered a prize of 20,000 pounds to anyone who could pull this off. That&rsquo;s about $4 Million today!</p>
                                <p>Well, in 1761 a clockmaker named John Harrison finally did it with his invention of the marine chronometer.</p>
                                <p>Captain James Cook gave Harrison&rsquo;s chronometer its first long run test by using it on his second and third voyages exploring the Pacific Ocean over a number of years.</p>
                                <p>Cook was delighted with it.</p>
                                <p>Because explorers could now easily determine their exact location in terms of both latitude and longitude, it became possible for Cook and then later Captain Vancouver, to draw very accurate maps.</p>
                                <p>Previous attempts to create a map without knowing a ships exact location was essentially guess work. Look at this early Russian map of the Northwest coast, and now compare it to this one here created by Captain Vancouver.</p>
                                <p>Landmarks and ocean depths along the coast line could be recorded in more detail than ever before by marking exact positions of longitude x and latitude Y</p>
                                <p>And that&rsquo;s how Captain Vancouver navigated the oceans hundreds of years ago!</p>
