<p>Sextant de cuivre à cinq oculaires, v. 1790.</p>

<p>Fabriqué par Jesse Ramsden, utilisé par Joseph Baker, lieutenant de Vancouver, et prêté au Vancouver Maritime Museum, courtoisie de Richard Baker. Le sextant est un instrument de navigation à double réflexion avec un arc gradué de 60° et un mécanisme de visée, utilisé pour mesurer les distances angulaires entre des objets et surtout pour calculer des altitudes en navigation.</p>
