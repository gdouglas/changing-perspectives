        <div id="card-0">
            <h2>Panneau 1</h2>
            <p>Cette bande dessinée est tirée de « L’album de 500 ans de résistance autochtone » (The 500 Years of Indigenous Resistance Comic Book) créé par l’artiste et historien Kwakwaka’wakw Gord Hill.</p>
            <p><strong>AVERTISSEMENT: </strong>Certaines images sont de nature très crue et peuvent être troublantes.</p>
        </div>
        <div id="card-1">
            <h2>Panneau 2</h2>
            <p id="panel-one-transcript">« À quoi ressemblaient les peuples autochtones qui rencontraient les commerçants de fourrure? » (Le titre est en blanc sur fond noir avec une police de caractères rappelant l’écriture à la craie. Toutes les images suivantes sont dessinées à la main et sont en noir et blanc.)</p>
        </div>
        <div id="card-2">
            <h2>Panneau 3</h2>
            <p id="panel-two-transcript">« Pendant plusieurs décennies, la traite des fourrures était la principale raison pour laquelle les bateaux européens et américains visitaient la région… Ce commerce a décimé la population de loutre de mer. Les équipages usaient parfois de la force pour mater les récalcitrants, même parfois en prenant des otages. » (Dessin à la main en noir et blanc de deux loutres de mer qui nagent.)</p>
        </div>
        <div id="card-3">
            <h2>Panneau 4</h2>
            <p id="panel-three-transcript">« Les Russes ont commencé à traiter avec les Aléoutes en 1741. Ils ont pris des familles en otage et ont forcé des chasseurs à récolter des loutres de mer. En 1763, les Aléoutes se sont révoltés et ont détruit 4 de 5 bateaux russes. En signe de représailles, plusieurs villages aléoutes ont été massacrés. » (Un Russe portant un chapeau de fourrure braque un mousquet devant un villageois Aléoute qui a les mains en l’air en signe de reddition. On aperçoit un voilier en arrière-plan.</p>
        </div>
        <div id="card-4">
            <h2>Panneau 5</h2>
            <p id="panel-four-transcript">« Comme les Russes avaient ravagé les populations aléoutes et de loutre de mer, ils se sont mis à se déplacer vers le sud en territoire Tlingit. Mais les Tlingit étaient beaucoup mieux armés en raison de leur commerce avec les bateaux britanniques et américains. Les articles de traite les plus prisés étaient les fusils et les munitions. Sur la côte, les femmes dirigeaient la plupart des négociations commerciales. » (Trois Tlingits armés de mousquets font face à un Européen sur un voilier. L’Européen porte une veste et est aussi armé d’un mousquet.)</p>
        </div>
        <div id="card-5">
            <h2>Panneau 6</h2>
            <p id="panel-five-transcript">« Avec l’augmentation du nombre de bateaux venant dans la région entre les années 1780 et 1790, les attaques se sont multipliées. Au cours des 30 années suivantes, des douzaines de bateaux ont été capturés. » (Des guerriers en armure traditionnelle, armés de lances et d’assommoirs, abordent un bateau occupé par des Européens.)</p>
        </div>
        <div id="card-6">
            <h2>Panneau 7</h2>
            <p id="panel-six-transcript">« En 1803, le navire américain Boston fut attaqué par des guerriers Mowachaht-Muchalaht qui tuèrent 25 des 27 membres de l’équipage. Les deux survivants ont été forcés d’identifier les têtes décapitées de l’équipage. Ils ont été soumis à l’esclavage pendant 2 ans… » (Il y a une rangée de têtes coupées posées sur le pont d’un bateau. Des guerriers portant des lances et des assommoirs leur font face tandis qu’un Américain pointe les têtes.)</p>
        </div>
        <div id="card-7">
            <h2>Panneau 8</h2>
            <p id="panel-seven-transcript">« En 1811, le Tonquin fut attaqué par des guerriers Tla-o-qui-aht dirigés par Wickaninnish, dans la baie de Clayoquot. Un survivant parmi les membres de l’équipage arma des explosifs avant de s’enfuir. L’explosion fit au moins 100 morts parmi les Tla-o-qui-aht. » (Trois Tla-o-qui-aht sont sur la rive et regardent avec surprise un bateau exploser.)</p>
        </div>
