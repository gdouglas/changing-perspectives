                        <p>The plaque reads: To the Mowachaht-Muchalaht First Nations, Yuquot has always been the centre of their social, political and economic world. Whaling was a vital part of the life of the Mowachaht-Muchalaht, and of all the Nuu-chah-nulth peoples. Near here once stood the Whalers Washing House, a unique ceremonial structure and the most significant monument to a purification ritual on the West Coast of North America. In the late 18th century, Yuquot became an important site of early contact between First Peoples and Europeans. Explorers and traders were attracted to this safe harbour, which they called Friendly Cove. As a result, Yuquot, also known as Nootka, developed into an important centre of trade and diplomacy, and it was briefly the site of Spain&#39;s only military establishment in present-day Canada. Yuquot became the focal point of the Nootka Sound Controversy of 1789-1794, when the rival interests of Great Britain and Spain brought those countries to the brink of war.</p>
