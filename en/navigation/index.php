                                <p>What were the vessels like that Captain Vancouver sailed in?</p>
                                <p>&nbsp;A European ship in the 1700&rsquo;s was made from a variety of different wood.</p>
                                <p>The frame of the vessel was made from White Oak, because of its strength and resistance to rotting.</p>
                                <p>The decks and hull planking were made from Cedar or Fir.</p>
                                <p>And Sitka Spruce was preferred for the masts and spars as they could more easily bend without breaking in heavy winds.</p>
                                <p>A mast would be &ldquo;stepped&rdquo; into the keel and then secured in place by shrouds and stays made from tar covered hemp rope.</p>
                                <p>Vessels like this needed a lot of looking after, so crew included a number of carpenters.</p>
                                <p>Masts and spars would frequently need to be replaced.</p>
                                <p>Leaks were a constant concern.</p>
                                <p>In Vancouver&rsquo;s time the vessel&rsquo;s bottom was also sheathed in copper to protect it against Teredo worm which is a saltwater clam that burrows into the timber.</p>
                                <p>The copper bottom also helped a ship to slip faster through the water.</p>
                                <p>European vessels were powered by the wind using a combination of four sided &ldquo;square&rdquo; sails and triangular &ldquo;Lateen&rdquo; sails made from canvas.</p>
                                <p>The design of these vessels evolved from Venetian, Roman and Viking boats that originally sailed along the European coastline.&nbsp;</p>
                                <p>By the 16th century, bigger vessels with enclosed decks were developed to cross the ocean.&nbsp; Square sails were more commonly used for long ocean crossings because they captured more wind.</p>
                                <p>On the downside, a square-rigged ship ideally needed to have the wind coming from behind or beside it.</p>
                                <p>Because sailors had to go aloft to change the sails, which means climbing up the rigging, these vessels required large crews.</p>
                                <p>There was also a strong possibility of losing seaman from falling off the rigging and going overboard. Sadly, it was not always possible to go back and find them.</p>
                                <p>Captain Vancouver had two vessels for his expedition, His Majesty&rsquo;s Ship Discovery (HMS Discovery for short) and HMS Chatham.</p>
                                <p>Both vessels were ideal for survey work; they had shallow drafts which meant they could get in close to shore.</p>
                                <p>They also had rounded nearly flat bottoms which meant they would be less likely to roll over if they did become grounded.</p>
                                <p>That&rsquo;s very handy when you sailing in unknown waters!</p>
                                <p>The Discovery was about 100 feet long and 28 feet across. It was very similar to Captain Cook&rsquo;s Ship &ndash; HMS Endeavour.</p>
                                <p>The design was based on a Collier - a ship that transports coal. Consequently, the hull was very strong and there was a large amount of space below deck.</p>
                                <p>The Chatham was half the size, 53 feet long and 22 feet across.</p>
                                <p>As a general rule, the bigger the sail area and the longer the vessel the faster it will go.</p>
                                <p>The Discovery, had a top speed of 8 nautical miles in one hour. That&rsquo;s about 15 Kilometers per hour.</p>
                                <p>The Chatham was a &ldquo;Brig&rsquo; which meant it was rigged mostly with square sails on two masts.</p>
                                <p>The Discovery was a &ldquo;Fully Rigged Ship&rdquo; which meant it was rigged mostly with square sails on three masts.</p>
                                <p>The British navy classified these vessels as &ldquo;Sloops of War&rdquo; which meant they had less than 20 large guns.</p>
                                <p>The Discovery had 10 guns and the Chatham had 4. By comparison, a first-rate British Navy warship from this period would have over 100 large guns and was roughly twice the size of Discovery.</p>
                                <p>The Discovery had a crew of 100 men which included 9 ship&rsquo;s officers, 38 able seamen to sail the ship, a military force of 16 marines, and skilled tradesmen such as carpenters, sailmakers, cooks, servants, gunners and a surgeon.</p>
                                <p>The Chatham had a crew of 45 in a similar combination of officers, seaman, marines and tradesmen. Life on board both vessels would have been very cramped.</p>
                                <p>Why are square sails uncommon today?</p>
                                <p>By the 1800&rsquo;s, multiple mast schooners were becoming popular particularly for trading and fishing vessels.</p>
                                <p>They employed a fore and aft rig.&nbsp; This meant that rather than having the sails positions across the ship using spars the sail ran along the length of the ship using booms.</p>
                                <p>Less crew was needed on a schooner because the sails could be changed on deck, no crew was needed to go aloft.</p>
                                <p>The fore and aft sails also allowed the vessel to sail closer to the wind, which means it could sail with the wind nearer to the bow of the ship.</p>
                                <p>This ultimately shortened the length of the journey.</p>
                                <p>Modern sailing yachts today still use a fore and aft sail rig. They also have metal masts and deeper keels which can support much larger sails.</p>
                                <p>Unlike vessels in Captain Vancouver&rsquo;s time, ships today usually have a lot fewer cannons</p>
